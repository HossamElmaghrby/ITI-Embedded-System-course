 
 // Z = 0000 1111 & 0001 0100 = 0000 0100----->4

 // k = 0000 1111 | 0001 0100 = 0001 1111----->31

 // l = 0000 1111 ^ 0001 0100 = 0001 1011----->27

 // m = 0000 1111 >> 3  = 0000 0000 0000 0001------>1

 // n = 0001 0100 << 4 =  0000 0001 0100 0000----->14144

#include<stdio.h>

void main()
{
	int x = 15 ;
	int y =20 ;

	int z,k,l,m,n ;

	z = x & y ;
	k = x | y ;
	l = x ^ y ;
	m = x>>3 ;
	n = y<<4 ;

	printf("Z = x & y = %d \nk = x | y = %d \nl = x ^ y = %d \n m = x>>3  = %d \nn = y<<4  = %d \n",z,k,l,m,n );
}
