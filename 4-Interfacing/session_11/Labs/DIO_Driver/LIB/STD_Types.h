#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef _Bool          		 	boolean;
typedef char          		  	char_t;
typedef signed char   		 	s8_t;
typedef unsigned char     	  	u8_t;
typedef signed short		 	s16_t;
typedef unsigned short 		 	u16_t;
typedef signed int 		 	    s32_t;
typedef unsigned int 		 	u32_t;
typedef signed long long int 	        s64_t;
typedef unsigned long long int          u64_t;

typedef volatile char          		  		vchar_t;
typedef volatile signed char   		 		vs8_t;
typedef volatile unsigned char     	  		vu8_t;
typedef volatile signed short		 		vs16_t;
typedef volatile  unsigned short 		 	vu16_t;
typedef volatile signed int 			 	vs32_t;
typedef volatile unsigned int 		 		vu32_t;
typedef volatile signed long long int 		        vs64_t;
typedef volatile unsigned long long int  	        vui64_t;

typedef float 		float32;
typedef double 		float64;

#endif /* STD_TYPES_H_ */
